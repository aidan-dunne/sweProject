			<h2>What</h2>
            <p>REUs are 10 week programs hosted at universities across the country. You're assigned a research advisor
                upon your acceptance, and that advisor has some kind of real-world project they'll guide you to work through, helping
                you learn whatever you need to know along the way. At the end of the program, you'll present your project and
                research. Along the way, you'll get to meet a lot of new people, explore a new campus, and do work not strictly
                limited to programming.
            </p>
			<h2>Why</h2>
			<p>REUs have a lot of cool benefits. You get to live somewhere new, do meaningful, applicable work, and probably
				go on a variety of adventures courtesy of your REU program. Most importantly, REUs are <b>more likely to accept
				freshmen than traditional internships</b>.
            </p>
            <h2>Where</h2>
            <p>REUs are hosted primarily at larger universities, but can theoretically happen anywhere. Larger universities
                tend to have larger programs, however.
            </p>
