			<h2>What are REUs?</h2>
            <p>REUs, or Research Experiences for Undergraduates, are 10 week programs hosted at universities across the country which provide you the 
			opportunity to research your area of interest in computer science. When you're accepted to an REU, you're assigned a research advisor who 
			is working on some sort of real-world research project. This advisor will guide you as you work through the project and will help you learn 
			everyting that you need to know along the way. At the end of the REU program, you'll have the chance to present your project and research. 
			As you work through your REU, you will get to meet many new people, explore a new campus, and learn about what computer science has offer 
			beyond just programming.</p>
			<h3>Why?</h3>
			<p>REUs have many great benefits to offer. First and foremost, if you're a freshman or sophomore struggling to find internships, an REU 
			may suit you better &mdash; REU programs are more likely to accept underclassmen while most internships will only accept rising seniors.
			Just like internships, REUs offer valuable experience and the opportunity to learn while introducing you to many talented people, and an REU 
			experience on your rèsumè is sure to impress when you do apply for internships in the future. Additionally, REUs can be exciting journeys. 
			You'll get to live somewhere new, do meaningful and practical work, and go on a variety of adventures courtesy of your REU program.</p>
            <h3>Where?</h3>
            <p>REUs are hosted primarily at larger universities, but can happen anywhere. Larger universities also tend to have larger programs, 
			however.</p>
            <h3>How?</h3>
			<p>If you'd like to learn more about what REUs are out there or want to find one to apply to yourself, the <a 
			href="https://www.nsf.gov/crssprgm/reu/reu_search.jsp" target="_blank" rel="noreferrer noopener">NSF's REU Database</a> is a great place to
			get your search started. Their database is large, descriptive, and allows you to both browse for REUs within a specific research field 
			and search for specific REUs of interest to you by using keywords. <br><br>If you're interested in REU programs but aren't really sure where to
			start or would otherwise like help applying, feel free to reach out to Andy Bernatow at andybernato@gmail.com for tips with applying! </p>
			<h2>Consider Truman!</h2>
			<p>While Truman does not offer traditional REU programs, research opportunities still exist for eager students. If you're interested in
			seeing what research opportunities Truman has to offer, you can visit <a href="https://osr.truman.edu/truscholars" target="_blank"
			rel="noreferrer noopener">Truman's TruScholars page</a> to learn about how to get involved with research right here at Truman.</p>
